SELECT  c.customer, 
        COALESCE(c.country, 'N/A') as country, 
        CAST(EXTRACT(MONTH FROM o.orderdate) AS INT) AS ordermonth,
        CAST(EXTRACT(YEAR FROM o.orderdate) AS INT) AS orderyear
FROM    customers AS c 
        INNER JOIN 
        orders AS o 
        ON c.customer = o.customer;

